#include "libft.h"

int	ft_isword(char c)
{
	return (c && !ft_is_whitespace(c) && c != '|'
		&& c != '<' && c != '>' && c != '$'
		&& c != '&' && c != '\''
		&& c != '\"' && c != '('
		&& c != ')' && c != ';');
}
